package com.kh;

public class Exam_02 {

	public static void main(String[] args) {
		//자료형 dataType
		boolean a = true;
		char b = 'A';
		String c = "khacademy";
		byte d = 127;
		short e = 32767;
		int f = 1000000000;
		long g = 1000000000000L; //접미사 L을 붙여야함 (대소문자 상관X)
		float h = 3.14F; //접미사 F를 붙여줘야함 (대소문자 상관X)
		double i = 5.12;
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		System.out.println(h);
		System.out.println(i); //변수 안에 있는 값을 출력할 때 " ", ' '를 사용하지 않는다.
		
		}

}
